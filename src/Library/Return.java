    public void delete() {
        String sql = "DELETE FROM `borrow_book` WHERE `libraryNumber`=?";
        try {
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, lno.getText());
            pstmt.execute();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private boolean checkInputs() {
        if (adm.getText().isEmpty() || sname.getText().isEmpty()
                || sclass.getText().isEmpty() || strm.getText().isEmpty()
                || rdate.getDate() == null || lno.getText().isEmpty()
                || isbn.getText().isEmpty() || title.getText().isEmpty()
                || yr.getText().isEmpty() || pub.getText().isEmpty()
                || status.getSelectedIndex() != -1 || isdate.getText().isEmpty()) {
            return false;
        } else {
            try {
                return true;
            } catch (Exception e) {
                return false;
            }
        }
    }

    public void insert() {
        if (checkInputs()) {
            JOptionPane.showMessageDialog(null, "Blank fields", "Blank", JOptionPane.ERROR_MESSAGE);
        } else {
            String sql = "INSERT INTO `return_book`(`admNo`, `Name`, `Form`,"
                    + " `Stream`, `borrowedDate`, `returnDate`, `libraryNumber`, `ISBN`, "
                    + "`Title`, `Year`, `Publisher`, `Availability`) VALUES (?,?,?,?,?,?,?,?,?,?,?,?)";
            try {
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, adm.getText());
                pstmt.setString(2, sname.getText());
                pstmt.setString(3, sclass.getText());
                pstmt.setString(4, strm.getText());
                pstmt.setString(5, isdate.getText());
                pstmt.setString(6, (new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(rdate.getDate())));
                pstmt.setString(7, lno.getText());
                pstmt.setString(8, isbn.getText());
                pstmt.setString(9, title.getText());
                pstmt.setString(10, yr.getText());
                pstmt.setString(11, pub.getText());
                pstmt.setString(12, status.getSelectedItem().toString());

                int r = pstmt.executeUpdate();
                if (r > 0) {
                    JOptionPane.showMessageDialog(null, "Book Returned Successfully", "Returned", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
     public void chckRecord() {
        String sql = "SELECT * FROM `borrow_book` WHERE `libraryNumber` = ?";
        try {

            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, lno.getText());

            rs = pstmt.executeQuery();
            boolean recordAdded = false;
            while (rs.next()) {
                recordAdded = true;
            }
            if (recordAdded) {
               insert();
            } else {
                JOptionPane.showMessageDialog(null, "Book Does Not Exists", "Not Found",
                        JOptionPane.ERROR_MESSAGE);

            }

        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, ex);
        }

    }

