    private void srchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srchActionPerformed
        String sql = "SELECT * FROM `borrow_book` WHERE `libraryNumber`=?";
        if (!lno.getText().isEmpty()) {
            try {
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, lno.getText());

                rs = pstmt.executeQuery();
                if (rs.next()) {
                    adm.setText(rs.getString("admNo"));
                    sname.setText(rs.getString("Name"));
                    sclass.setText(rs.getString("Form"));
                    strm.setText(rs.getString("Stream"));
                    isdate.setText(rs.getString("borrowedDate"));
                    isbn.setText(rs.getString("ISBN"));
                    title.setText(rs.getString("Title"));
                    yr.setText(rs.getString("Year"));
                    pub.setText(rs.getString("Publisher"));
                    rs.close();
                    pstmt.close();
                } else {
                    JOptionPane.showMessageDialog(null, "Book record not found. Book has not been issued", "Not Found", JOptionPane.ERROR_MESSAGE);
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    rs.close();
                    pstmt.close();
                } catch (SQLException e) {
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Blank admission number", "Blank", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_srchActionPerformed

