    private void srchStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srchStudentActionPerformed
        String sql = "SELECT * FROM `student` WHERE `admNo`=?";
        if (!adm.getText().isEmpty()) {
            try {
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, adm.getText());

                rs = pstmt.executeQuery();
                if (rs.next()) {
                    sname.setText(rs.getString("otherName"));
                    sclass.setText(rs.getString("Form"));
                    strm.setText(rs.getString("Stream"));
                    rs.close();
                    pstmt.close();
                } else {
                    JOptionPane.showMessageDialog(null, "Student not found", "Not Found", JOptionPane.ERROR_MESSAGE);
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    rs.close();
                    pstmt.close();
                } catch (SQLException e) {
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Blank admission number", "Blank", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_srchStudentActionPerformed

