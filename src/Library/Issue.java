    private boolean checkInputs() {
        if (adm.getText().isEmpty() || sname.getText().isEmpty()
                || sclass.getText().isEmpty() || strm.getText().isEmpty()
                || bdate.getDate() == null || lno.getText().isEmpty()
                || isbn.getText().isEmpty() || title.getText().isEmpty()
                || yr.getText().isEmpty() || pub.getText().isEmpty()
                || status.getSelectedIndex() != -1) {
            return false;
        } else {
            try {
                return true;
            } catch (Exception e) {
                return false;
            }
        }
    }

    public void issue() {
        String sql = "INSERT INTO `borrow_book`(`admNo`, `Name`, `Form`,"
                + " `Stream`, `borrowedDate`, `libraryNumber`, `ISBN`, `Title`, "
                + "`Year`, `Publisher`, `Availability`) VALUES (?,?,?,?,?,?,?,?,?,?,?)";
        if (!checkInputs()) {
            try {
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, adm.getText());
                pstmt.setString(2, sname.getText());
                pstmt.setString(3, sclass.getText());
                pstmt.setString(4, strm.getText());
                pstmt.setString(5, (new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(bdate.getDate())));
                pstmt.setString(6, lno.getText());
                pstmt.setString(7, isbn.getText());
                pstmt.setString(8, title.getText());
                pstmt.setString(9, yr.getText());
                pstmt.setString(10, pub.getText());
                pstmt.setString(11, (String) status.getSelectedItem());

                int a = pstmt.executeUpdate();
                if (a > 0) {
                    JOptionPane.showMessageDialog(null, "Issued Successfully", "Issued", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Blank fields, Fill in the fields and try again", "Blank", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void oneUser() {
        String sql = "SELECT * FROM `staff_issued` WHERE `libraryNumber`= ?";

        try {
            pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, lno.getText());

            rs = pstmt.executeQuery();

            boolean issued = false;
            while (rs.next()) {

                issued = true;
                JOptionPane.showMessageDialog(null, "The Book has been issued to\t"
                        + rs.getString("") + "", "Can't Be Issued", JOptionPane.ERROR_MESSAGE);
            }
            if (issued) {
                issue();
            } else {
                JOptionPane.showMessageDialog(null, "Book available for issuance");
            }

        } catch (SQLException e) {
        }
    }

